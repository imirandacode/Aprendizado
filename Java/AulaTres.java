package Java;

public class AulaTres {
    public static void main(String[] args) {

        int a;
        int b = 2;
        a = 3;

        int soma = a + b ;
        int multiplicacao = a * b ;
        int subtracao = a - b ;
        float divisao = (float) a / b ;

        System.out.println("O Resultado da soma entre os A e B é "+ soma);
        System.out.println("O Resultado da subtração entre os A e B é "+ subtracao);
        System.out.println("O Resultado da multiplicação entre os A e B é "+ multiplicacao);
        System.out.println("O Resultado da divisão entre os A e B é "+ divisao);

    }
}
